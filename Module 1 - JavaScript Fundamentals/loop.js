// for loop: executes a block of code a specified number of time 
console.log("for loop:"); 
for (let i = 0; i < 5; i++) {
    console.log("Iteration:", i); 
}

// while loop: executes a block of code as long as a condition is ture 
console.log("\nwhile loop:"); 
let count = 0; 
while (count < 3) {
    console.log("Count:", count); 
    count++; 
}

// do...while loop: executes a block of code at least once, and then repeats as long as a condition is ture. 
console.log("\ndo..while loop:"); 
let j = 0; 
do {
    console.log("j:", j); 
    j++; 
} while (j < 2); 
