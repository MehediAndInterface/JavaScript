// Imagine you have a box, and you want to put something inside it. In JavaScript, a variable is like that box.

// var
var myVar = 10; 
var myString = "Hello"
var myBoolean = true; 

// let 
let myLetVar = 20; 
myLetVar = 25; // reassignment is allowed 

// const 
const myConstVar = 30; 
// myConstVar = 25; // this would case an error

//Important notes:
//1. var has function scope, let and const have block scope.
//2. const variables cannot be reassigned after initialization.
//3. let variables can be reassigned.
//4. Variables declared without any keyword are implicitly global, which is generally bad practice.


